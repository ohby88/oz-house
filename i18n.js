let currentLang = 'ko';

function setLang(lang) {
    currentLang = lang;
    document.documentElement.lang = lang;
    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        if (T[key] && T[key][lang]) {
            el.innerHTML = T[key][lang];
        }
    });
    // 언어 선택 버튼 활성화
    document.querySelectorAll('.site-lang-btn').forEach(btn => {
        btn.classList.toggle('active', btn.getAttribute('data-lang') === lang);
    });
    // 배달 섹션도 동기화
    document.querySelectorAll('.delivery-content').forEach(el => el.classList.remove('active'));
    document.querySelectorAll('.lang-tab').forEach(el => el.classList.remove('active'));
    const dlMap = { ko: 'ko', en: 'en', zh: 'zh', ja: 'ja' };
    const dlEl = document.getElementById('lang-' + dlMap[lang]);
    if (dlEl) dlEl.classList.add('active');
    document.querySelectorAll('.lang-tab').forEach(btn => {
        if (btn.textContent.includes(lang === 'ko' ? '한국어' : lang === 'en' ? 'English' : lang === 'zh' ? '中文' : '日本語')) {
            btn.classList.add('active');
        }
    });
    localStorage.setItem('oz-lang', lang);
}

// 페이지 로드 시 저장된 언어 또는 브라우저 언어 감지
document.addEventListener('DOMContentLoaded', function() {
    const saved = localStorage.getItem('oz-lang');
    if (saved && T.nav_about[saved]) {
        setLang(saved);
    } else {
        const bl = navigator.language.slice(0, 2);
        if (T.nav_about[bl]) setLang(bl);
    }
});
